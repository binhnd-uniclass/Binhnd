export const greetingsByHour = (hour, name) => {
  const safeName = name?.split(' ').slice(-1)[0] || 'bạn'
  if (hour < 11) return `Chào buổi sáng, ${safeName}! ☀️`
  if (hour < 14) return `Chào trưa, ${safeName}! 🌤`
  if (hour < 18) return `Chào buổi chiều, ${safeName}! 🌅`
  return `Chào tối, ${safeName}! 🌙`
}
