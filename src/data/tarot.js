// Lấy ngẫu nhiên 1 lá theo seed (để bài "của ngày" cố định trong ngày đó)
export const drawCardBySeed = (seed) => {
  const idx = Math.abs(seed) % tarot.length
  return tarot[idx]
}

// Lấy ngẫu nhiên 1 lá thật (cho rút thêm tham khảo)
export const drawRandomCard = () => {
  return tarot[Math.floor(Math.random() * tarot.length)]
}
